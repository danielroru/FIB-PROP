    private void addBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBActionPerformed
        DefaultTableModel model = (DefaultTableModel)songTable.getModel();
        
        if(songTable.getSelectedRow() == -1){
            if(songTable.getRowCount() == 0) error.setText("No hay canciones en el sistema");
            else error.setText("Debes seleccionar una Canción");
        }
        else {
            
            String Tit = model.getValueAt(songTable.getSelectedRow(), 0).toString();
            String Aut = model.getValueAt(songTable.getSelectedRow(), 1).toString();
            int id = CD.getIdCancion(Tit, Aut);
            if(canciones.contains(id)) error.setText("El usuario " + user + " ya tiene la cancion " + Tit );
            else {
                canciones.add(id);
                error.setText("Cancion: " + Tit +  " Añadida al usuario: " + user);
            }
            
        }
    }//GEN-LAST:event_addBActionPerformed

