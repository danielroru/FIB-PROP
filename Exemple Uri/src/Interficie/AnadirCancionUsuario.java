    private void buscarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBActionPerformed
        DefaultTableModel model = (DefaultTableModel)songTable.getModel();
        
        ArrayList<Object> obj = CD.cargarCancionBuscador(buscarTitulo.getText(),buscarAutor.getText());
        model.setRowCount(0);
        for(int i = 0; i < obj.size(); ++i){
            Object res[] = (Object[]) obj.get(i);
            model.addRow(res);
        }
        if(!buscarTitulo.getText().equals("") || !buscarAutor.getText().equals("")){
            showAllB.setVisible(true);
        }
    }//GEN-LAST:event_buscarBActionPerformed

